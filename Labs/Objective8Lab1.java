public class Objective8Lab1 {
  public static void main(String[] args) {

    //For loop creates the variable.
    //Prints the number, adds one then runs again.
    //Continues until it greater then or equal to 10.
    for (int num = 1; num <= 10; num++) {
      System.out.println(num);
    }
    //For loop creates the variable.
    //Prints the number, subtracts one then runs again.
    //Continues until it less then or equal to 1.
    for (int num = 10; num >=1; num --) {
      System.out.println(num);
    }
  }
}
